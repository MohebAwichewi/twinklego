create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, full_name, avatar_url, role)
  values (
    new.id,
    coalesce(new.raw_user_meta_data->>'full_name', ''),
    coalesce(new.raw_user_meta_data->>'avatar_url', null),
    coalesce(new.raw_user_meta_data->>'role', 'customer')
  )
  on conflict (id) do nothing;

  insert into public.wallets (user_id)
  values (new.id)
  on conflict (user_id) do nothing;

  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute procedure public.handle_new_user();

create or replace function public.update_user_rating()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  update public.profiles
  set
    rating = (select coalesce(round(avg(rating)::numeric, 1), 0) from public.reviews where reviewee_id = new.reviewee_id),
    rating_count = (select count(*) from public.reviews where reviewee_id = new.reviewee_id),
    updated_at = now()
  where id = new.reviewee_id;
  return new;
end;
$$;

drop trigger if exists on_review_created on public.reviews;
create trigger on_review_created
  after insert on public.reviews
  for each row execute procedure public.update_user_rating();

create or replace function public.adjust_wallet_balance(
  transaction_type text,
  transaction_amount numeric
)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  current_user_id uuid := auth.uid();
  user_wallet public.wallets%rowtype;
  signed_amount numeric;
begin
  if current_user_id is null then
    raise exception 'Not authenticated';
  end if;

  if transaction_type not in ('deposit', 'withdrawal') or transaction_amount <= 0 then
    raise exception 'Invalid wallet transaction';
  end if;

  select * into user_wallet from public.wallets where user_id = current_user_id for update;
  if not found then
    insert into public.wallets (user_id) values (current_user_id)
    returning * into user_wallet;
  end if;

  signed_amount := case when transaction_type = 'withdrawal' then -transaction_amount else transaction_amount end;

  if transaction_type = 'withdrawal' and user_wallet.balance < transaction_amount then
    raise exception 'Insufficient wallet balance';
  end if;

  update public.wallets
  set balance = balance + signed_amount
  where id = user_wallet.id;

  insert into public.transactions (wallet_id, type, amount, description)
  values (
    user_wallet.id,
    transaction_type,
    signed_amount,
    case when transaction_type = 'deposit' then 'Manual wallet top-up' else 'Manual withdrawal request' end
  );
end;
$$;

create or replace function public.complete_errand(target_errand_id bigint)
returns public.errands
language plpgsql
security definer
set search_path = public
as $$
declare
  current_user_id uuid := auth.uid();
  target public.errands%rowtype;
  customer_wallet public.wallets%rowtype;
  runner_wallet public.wallets%rowtype;
  task_price numeric;
begin
  if current_user_id is null then
    raise exception 'Not authenticated';
  end if;

  select * into target
  from public.errands
  where id = target_errand_id
  for update;

  if not found then
    raise exception 'Errand not found';
  end if;

  if target.assigned_runner_id is distinct from current_user_id then
    raise exception 'Only the assigned runner can complete this errand';
  end if;

  if target.status <> 'in_progress' then
    raise exception 'Errand must be in progress before completion';
  end if;

  task_price := coalesce(target.price, 0);

  select * into customer_wallet from public.wallets where user_id = target.customer_id for update;
  if not found then
    insert into public.wallets (user_id) values (target.customer_id)
    returning * into customer_wallet;
  end if;

  select * into runner_wallet from public.wallets where user_id = target.assigned_runner_id for update;
  if not found then
    insert into public.wallets (user_id) values (target.assigned_runner_id)
    returning * into runner_wallet;
  end if;

  if customer_wallet.balance < task_price then
    raise exception 'Customer wallet has insufficient balance';
  end if;

  update public.wallets set balance = balance - task_price where id = customer_wallet.id;
  update public.wallets set balance = balance + task_price where id = runner_wallet.id;

  insert into public.transactions (wallet_id, type, amount, description, related_errand_id)
  values
    (customer_wallet.id, 'payment', -task_price, 'Payment for: ' || target.title, target.id),
    (runner_wallet.id, 'earning', task_price, 'Earning from: ' || target.title, target.id);

  update public.errands
  set status = 'completed', completed_at = now()
  where id = target.id
  returning * into target;

  insert into public.task_tracking (
    errand_id,
    phase,
    delivered_at,
    eta_minutes,
    distance_to_next_km,
    updated_at
  )
  values (target.id, 'delivered', now(), 0, 0, now())
  on conflict (errand_id) do update
  set
    phase = 'delivered',
    delivered_at = coalesce(public.task_tracking.delivered_at, now()),
    eta_minutes = 0,
    distance_to_next_km = 0,
    updated_at = now();

  return target;
end;
$$;

