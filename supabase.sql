create table if not exists public.waitlist (
  id bigint generated by default as identity primary key,
  email text unique not null,
  interest text not null default 'help',
  created_at timestamptz not null default now()
);

create table if not exists public.profiles (
  id uuid references auth.users on delete cascade primary key,
  full_name text,
  phone text,
  address text,
  role text not null default 'customer' check (role in ('customer', 'runner', 'both')),
  avatar_url text,
  is_verified boolean not null default false,
  phone_verified boolean not null default false,
  address_verified boolean not null default false,
  rating numeric(2,1) default 0,
  rating_count int default 0,
  lat numeric(10,7),
  lng numeric(10,7),
  is_available boolean not null default false,
  emergency_contact_name text,
  emergency_contact_phone text,
  is_admin boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

alter table public.profiles add column if not exists phone_verified boolean not null default false;
alter table public.profiles add column if not exists address_verified boolean not null default false;

create table if not exists public.verifications (
  id bigint generated by default as identity primary key,
  user_id uuid references public.profiles(id) on delete cascade not null,
  id_type text not null,
  id_number text not null,
  id_image_url text,
  status text not null default 'pending' check (status in ('pending', 'approved', 'rejected')),
  reviewer_notes text,
  reviewed_by uuid references public.profiles(id),
  reviewed_at timestamptz,
  created_at timestamptz not null default now()
);

create table if not exists public.errands (
  id bigint generated by default as identity primary key,
  customer_id uuid references public.profiles(id) on delete cascade not null,
  title text not null,
  description text,
  category text not null default 'errand' check (category in ('groceries', 'delivery', 'home_help', 'errand', 'temporary_job', 'service_request')),
  pickup_lat numeric(10,7),
  pickup_lng numeric(10,7),
  dropoff_lat numeric(10,7),
  dropoff_lng numeric(10,7),
  pickup_address text,
  dropoff_address text,
  status text not null default 'posted' check (status in ('posted', 'accepted', 'in_progress', 'completed', 'cancelled', 'disputed')),
  price numeric(10,2) not null default 0,
  distance_km numeric(8,2),
  assigned_runner_id uuid references public.profiles(id),
  completed_at timestamptz,
  created_at timestamptz not null default now()
);

do $$
begin
  if exists (
    select 1 from information_schema.table_constraints
    where constraint_schema = 'public'
      and table_name = 'errands'
      and constraint_name = 'errands_category_check'
  ) then
    alter table public.errands drop constraint errands_category_check;
  end if;

  alter table public.errands
    add constraint errands_category_check
    check (category in ('groceries', 'delivery', 'home_help', 'errand', 'temporary_job', 'service_request'));
end $$;

create table if not exists public.wallets (
  id bigint generated by default as identity primary key,
  user_id uuid references public.profiles(id) on delete cascade unique not null,
  balance numeric(12,2) not null default 0,
  created_at timestamptz not null default now()
);

create table if not exists public.transactions (
  id bigint generated by default as identity primary key,
  wallet_id bigint references public.wallets(id) on delete cascade not null,
  type text not null check (type in ('deposit', 'withdrawal', 'payment', 'earning', 'refund')),
  amount numeric(12,2) not null,
  description text,
  related_errand_id bigint references public.errands(id),
  created_at timestamptz not null default now()
);

create table if not exists public.reviews (
  id bigint generated by default as identity primary key,
  errand_id bigint references public.errands(id) on delete cascade not null,
  reviewer_id uuid references public.profiles(id) on delete cascade not null,
  reviewee_id uuid references public.profiles(id) on delete cascade not null,
  rating int not null check (rating between 1 and 5),
  comment text,
  created_at timestamptz not null default now(),
  unique(errand_id, reviewer_id)
);

create table if not exists public.disputes (
  id bigint generated by default as identity primary key,
  errand_id bigint references public.errands(id) on delete cascade not null,
  raised_by uuid references public.profiles(id) on delete cascade not null,
  reason text not null,
  category text not null default 'other' check (category in ('service_issue', 'no_show', 'safety_concern', 'other')),
  status text not null default 'open' check (status in ('open', 'investigating', 'resolved')),
  resolution text,
  resolved_by uuid references public.profiles(id),
  resolved_at timestamptz,
  created_at timestamptz not null default now()
);

create table if not exists public.gps_records (
  id bigint generated by default as identity primary key,
  user_id uuid references public.profiles(id) on delete cascade not null,
  lat numeric(10,7) not null,
  lng numeric(10,7) not null,
  recorded_at timestamptz not null default now()
);

create table if not exists public.task_tracking (
  id bigint generated by default as identity primary key,
  errand_id bigint references public.errands(id) on delete cascade unique not null,
  phase text not null default 'accepted' check (phase in ('accepted', 'heading_to_pickup', 'picked_up', 'en_route_delivery', 'delivered')),
  runner_lat numeric(10,7),
  runner_lng numeric(10,7),
  distance_to_next_km numeric(8,2),
  eta_minutes int,
  accepted_at timestamptz,
  heading_to_pickup_at timestamptz,
  picked_up_at timestamptz,
  en_route_delivery_at timestamptz,
  delivered_at timestamptz,
  last_location_at timestamptz,
  updated_at timestamptz not null default now()
);

create table if not exists public.notifications (
  id bigint generated by default as identity primary key,
  user_id uuid references public.profiles(id) on delete cascade not null,
  title text not null,
  body text,
  is_read boolean not null default false,
  related_errand_id bigint references public.errands(id),
  created_at timestamptz not null default now()
);

